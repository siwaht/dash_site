const VideoContext = createContext<VideoContextType | undefined>(undefined);

export function VideoProvider({ children }: { children: React.ReactNode }) {
    const [videos, setVideos] = useState<Record<string, VideoData>>(() => {
        const saved = localStorage.getItem('siwaht_videos');
        if (saved) {
            try {
                const parsed = JSON.parse(saved);
                // Merge with defaults to ensure all required sections exist
                // We map over default keys to ensure we have at least those, 
                // but we prefer the parsed values if they exist.
                const merged = { ...defaultVideos };
                Object.keys(parsed).forEach(key => {
                    if (merged[key]) {
                        merged[key] = { ...merged[key], ...parsed[key] };
                    }
                });
                return merged;
            } catch (error) {
                console.error('Failed to parse saved videos:', error);
                return defaultVideos;
            }
        }
        return defaultVideos;
    });

    useEffect(() => {
        localStorage.setItem('siwaht_videos', JSON.stringify(videos));
    }, [videos]);

    const updateVideo = (id: string, data: Partial<VideoData>) => {
        setVideos(prev => ({
            ...prev,
            [id]: { ...prev[id], ...data }
        }));
    };

    const saveVideo = (id: string): { success: boolean; error?: string } => {
        try {
            const video = videos[id];
            if (!video) {
                return { success: false, error: 'Video not found' };
            }

            localStorage.setItem('siwaht_videos', JSON.stringify(videos));
            return { success: true };
        } catch (error) {
            console.error('Error saving video:', error);
            return {
                success: false,
                error: error instanceof Error ? error.message : 'Failed to save video'
            };
        }
    };

    return (
        <VideoContext.Provider value={{ videos, updateVideo, saveVideo }}>
            {children}
        </VideoContext.Provider>
    );
}

export function useVideos() {
    const context = useContext(VideoContext);
    if (context === undefined) {
        throw new Error('useVideos must be used within a VideoProvider');
    }
    return context;
}
